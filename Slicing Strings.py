#Slicing Strings
a = "Hello, World!"
print(a[2:8])

#Slice from the Start
print(a[:5])

#Slice from the End
print(a[3:])

#Negative Indexing
print(a[-5:-2])

#Modify Strings
#upper()
print(a.upper())

#lower()
print(a.lower())

#remove white space
print(a.strip())

#replace
print(a.replace("H","J"))

#Split - Substrings
print(a.split(","))