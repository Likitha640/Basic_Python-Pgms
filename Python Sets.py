#Python Numbers
x=3
y=4.5
z=3+2j
print(type(x))
print(type(y))
print(type(z))

#Type Conversion int to float
a=float(x)
print(a)
print(type(a))

#Type Conversion float to int
b=int(y)
print(b)
print(type(b))

#Type Conversion float to complex
c=complex(y)
print(c)
print(type(c))

#You cannot convert complex numbers into another number type.
