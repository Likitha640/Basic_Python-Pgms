#Python Casting
#float()
a = float(1)     # a will be 1.0
b = float(6.3)   # b will be 6.3
c = float("3")   # c will be 3.0
d = float("1.6") # d will be 1.6

print(a)
print(b)
print(c)
print(d)

#int()

x = int(1)     # x will be 1
y = int(2.3)   # y will be 2
z = int("630") # z will be 630

print(x)
print(y)
print(z)

#str()

p = str("L1") # x will be 'L1'
q = str(6)    # y will be '6'
r = str(3.0)  # z will be '3.0'

print(p)
print(q)
print(r)